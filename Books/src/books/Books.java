    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        if (!tfTitle.getText().isEmpty() && !tfWriter.getText().isEmpty()
                && !tfFormat.getText().isEmpty()) {

            String writer = tfWriter.getText();
            String title = tfTitle.getText();
            String series = tfSeries.getText();
            String format = tfFormat.getText();
            modelBooks.addRow(new Object[]{writer, title, series, format});//csinálok egy Object nevű 
            //tömböt az adatoknak amiket fel akarok vinni a táblázatba

           // tfWriter.setText(" ");
            tfTitle.setText(" ");
            //tfSeries.setText(" ");
            //tfFormat.setText(" ");

            Connection conn = null;
            PreparedStatement pstmt = null;

            try {
                Class.forName("com.mysql.jdbc.Driver");
                conn = DriverManager.getConnection("jdbc:mysql:///Books", "root", "Lyel_Sz00");
                pstmt = conn.prepareStatement("insert into Books values(?,?,?,?)");
                pstmt.setString(1, writer);
                pstmt.setString(2, title);
                pstmt.setString(3, format);
                pstmt.setString(4, series);
                int i = pstmt.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "Könyv mentve!");
                } else {
                    JOptionPane.showMessageDialog(null, "Nem sikerült a mentés!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);

            }
        } else {
            JOptionPane.showMessageDialog(this, "Üres mező!", "Hiba!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAddActionPerformed

