    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (tblBooks.getSelectedRow() == -1) {
            if (tblBooks.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "A táblázat üres!");
            } else {
                JOptionPane.showMessageDialog(this, "Válassz egy könyvet!");
            }
        } else {
            modelBooks.removeRow(tblBooks.getSelectedRow());
            int reply = JOptionPane.showConfirmDialog(this, "Valóban töröljük?", "Törlés megerősítése", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_NO_OPTION) {
                tfWriter.setText(" ");
                tfTitle.setText(" ");
                tfSeries.setText(" ");
               tfFormat.setText(" ");

            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (tblBooks.getSelectedRow() == -1) {
            if (tblBooks.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "A táblázat üres!");
            } else {
                JOptionPane.showMessageDialog(this, "Válassz egy könyvet!");
            }
        } else {
            modelBooks.setValueAt(tfWriter.getText(), tblBooks.getSelectedRow(), 0);
            modelBooks.setValueAt(tfTitle.getText(), tblBooks.getSelectedRow(), 1);
            modelBooks.setValueAt(tfSeries.getText(), tblBooks.getSelectedRow(), 2);
            modelBooks.setValueAt(tfFormat.getText(), tblBooks.getSelectedRow(), 3);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

